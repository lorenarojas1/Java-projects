public class WhileLoopExample {
    public static void main(String[] args) {
        int x= 1;
        while (x <= 10){
            System.out.println(++x);
        }

        int i = 7;
        char c = 'w';
        System.out.println((i >= 6) && (c == 'w'));

        int a=1, j=2, k=3, m=2;
        System.out.println ((j >= a) || (k == m));
    }

}
